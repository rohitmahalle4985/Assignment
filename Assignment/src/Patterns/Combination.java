package Patterns;

public class Combination {
public static void main(String[] args) {
	for(int i=1;i<=5;i++)
	{
		int no=1;
		//System.out.print(i);
		for(int j=i;j<=5;j++)
		{	
			
			System.out.print(no);
			no=no+1;
			
		}
		
		System.out.println();	
	}
	for(int i=1;i<=5;i++)
		{
		for(int j=1;j<=5;j++)
		{
			
		
			System.out.print("  ");
	}
	System.out.println();
	
}
}
}