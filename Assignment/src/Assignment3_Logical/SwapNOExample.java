package Assignment3_Logical;

public class SwapNOExample {
 public static void main(String[] args) {
	int num1=10;
	int num2=20;
	System.out.println("Before swapping");
	System.out.println("Value of no.1="+num1);
	System.out.println("Value of no.2="+num2);
	int temp=num1;
	 num1=num2;
	 num2=temp;
	 System.out.println("After Swapping Values");
	 System.out.println("Value of no.1="+num1);
	 System.out.println("Value of no.2="+num2);
	
}
}
