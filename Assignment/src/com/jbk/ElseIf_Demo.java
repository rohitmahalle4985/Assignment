package com.jbk;

public class ElseIf_Demo {
	public static void main(String[] args) {
		
		int num=1;
		if(num==1)
		{
			System.out.println("one");
		}
		else if(num==2)
		{
			System.out.println("Two");
		}
		else if(num==3)
		{
			System.out.println("Three");
		}
		else
		{
			System.out.println("Wrong Input Given");
		}
	}

}
